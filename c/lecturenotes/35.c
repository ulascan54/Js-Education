/******************************************************************************
Print first 20 Fibonacci Numbers: F(0): 1 F(1): 1 ... F(n) = F(n-2) + F(n-1) for n>1
1 1 2 3 5 8 13 ...
*******************************************************************************/
#include <stdio.h>

int main()
{
    int i = 2, prev = 1, last = 1, current;
    printf("F(0):%d F(1):%d ", prev, last);
    for (; i <= 20; i++)
    {
        current = prev + last;
        printf("F(%d):%d ", i, current);
        prev = last;
        last = current;
    }
}

